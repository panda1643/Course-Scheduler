    private void adminDisplayCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminDisplayCourseButtonActionPerformed
        // TODO add your handling code here:
        String courseSelected = adminDisplayCourseComboBox.getSelectedItem().toString();
        ArrayList<ScheduleEntry> ScheduledEntries = ScheduleQueries.getScheduledStudentsByCourse(currentSemester, courseSelected);
        ArrayList<ScheduleEntry> WaitlistedEntries = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, courseSelected);
        
        DefaultTableModel displayScheduleTableModel = (DefaultTableModel)adminDisplayScheduledTable.getModel();

        displayScheduleTableModel.setNumRows(0);
        Object[] rowData = new Object[3];

        for (ScheduleEntry entry:ScheduledEntries)
        {
            StudentEntry student = StudentQueries.getStudent(entry.getStudentid());
            String SFirstName = student.getFirstName();
            String SLastName = student.getLastName();
            String studentID = entry.getStudentid();
            
            rowData[0] = SLastName;
            rowData[1] = SFirstName;
            rowData[2] = studentID;
            
            displayScheduleTableModel.addRow(rowData);

        }
        
        DefaultTableModel displayWaitlistTableModel = (DefaultTableModel)adminDisplayWaitlistedTable.getModel();

        displayWaitlistTableModel.setNumRows(0);
        Object[] rowData_ = new Object[3];

        for (ScheduleEntry entry:WaitlistedEntries)
        {
            StudentEntry student = StudentQueries.getStudent(entry.getStudentid());
            String SFirstName = student.getFirstName();
            String SLastName = student.getLastName();
            String studentID = entry.getStudentid();
            
            rowData_[0] = SLastName;
            rowData_[1] = SFirstName;
            rowData_[2] = studentID;
            
            displayWaitlistTableModel.addRow(rowData_);

        }
    }//GEN-LAST:event_adminDisplayCourseButtonActionPerformed

