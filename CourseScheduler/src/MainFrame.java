    private void scheduleCoursesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleCoursesButtonActionPerformed
        // TODO add your handling code here:
        String  CourseCodeSelected = currentSelectCourseCB.getSelectedItem().toString();
        String  SelectedStudentname = currentStudentCB.getSelectedItem().toString();
        int reservedSeats = ScheduleQueries.getScheduledStudentCount(currentSemester, CourseCodeSelected);
        int seatsPossible = CourseQueries.getCourseSeats(currentSemester, CourseCodeSelected);

        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        String SelectedStudentID = students.get(currentStudentCB.getSelectedIndex()).getStudentid();

        //code for timestamp
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());

        if(reservedSeats<seatsPossible){
            ScheduleEntry student = new ScheduleEntry(currentSemester, CourseCodeSelected, SelectedStudentID, "s", currentTimestamp);
            ScheduleQueries.addScheduleEntry(student);
            scheduleCoursesLabel.setText(SelectedStudentname+" has been scheduled for the class.");

        }
        else{
            ScheduleEntry student = new ScheduleEntry(currentSemester, CourseCodeSelected, SelectedStudentID, "w", currentTimestamp);
            ScheduleQueries.addScheduleEntry(student);
            scheduleCoursesLabel.setText(SelectedStudentname+" has been waitlisted for the class.");
        }
        rebuildScheduleComboBox();
        rebuildDropComboBoxes(SelectedStudentID);
    }//GEN-LAST:event_scheduleCoursesButtonActionPerformed

